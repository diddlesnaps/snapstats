<h1>Snapstats.org privacy policy</h1>

<h2>What data we collect</h2>
<p>
    We collect data from the public Snap Store API. Any publically accessible
    data available from the Snap Store is stored for analytical and reference
    purposes. This data is made available for further public access.
</p>
<p>
    We utilise Google Analytics code for tracking usage of the Snapstats.org
    website. You may find out what data Google collects from the Analytics
    product via
    <a href="https://policies.google.com/privacy">Google's Privacy Policy</a>.
    We have stipulated that Google must retain this data in anonymous form and
    must delete it after 2 months from collection.
</p>
<p>
    If you share pages from this site onto Facebook or interact with the
    Snapstats.org page on Facebook then we receive analytical data about what
    you shared or interactions with the page. More information can be found in
    <a href="https://www.facebook.com/about/privacy">Facebook's Privacy Policy</a>.
    We <strong>do not</strong> include active Facebook tracking code on the
    Snapstats.org website.
</p>

<h2>What we do with the data</h2>
<p>
    We use this data purely for analytical purposes. We may share limited
    analytical data publicly, such as the number of visits to a particular
    page. These communications might include promotional articles or messages
    that are intended to boost awareness of Snapstats. Any such messaging will
    only ever include truly anonymous, aggregated, statistics.
</p>
